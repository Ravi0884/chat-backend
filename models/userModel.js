const mongoose = require("mongoose");
//-------------------------------- With the help of Schema we are defining our inputs from user-------------
const userSchema = new mongoose.Schema({
  username: {
    type: String,
    required: true,
    min: 3,
    max: 20,
    unique: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
    max: 50,
  },
  password: {
    type: String,
    required: true,
    min: 8,
  },
  isAvatarImageSet: {
    type: Boolean,
    default: false,
  },
  avatarImage: {
    type: String,
    default: "",
  },
});

//  ------------------Save our collections with the name of Users ---------------------

module.exports = mongoose.model("Users", userSchema);
